

function col(str) {
    return '<col>'+str+'</col>';
}

function row(str) {
    return '<row>'+str+'</row>';
}

function ul(str) {
    return '<ul>'+str+'</ul>';
}

function li(str) {
    return '<li>'+str+'</li>';
}

function span(str){
    return '<span>'+str+'</span>';
}

function spanRed(str){
    return '<span style="color: red;">'+str+'</span>';
}

function spanGreen(str){
    return '<span style="color: green;">'+str+'</span>';
}